def get_review_system_prompt(contract_type: ContractType | None = None) -> str:
    """계약 유형에 맞는 검토 시스템 프롬프트를 반환합니다."""
    if contract_type and contract_type in _LAW_CONTEXT:
        law_context = _LAW_CONTEXT[contract_type]
    else:
        # 유형 미결정 시 모든 법령 컨텍스트 포함
        law_context = "\n".join(_LAW_CONTEXT.values())

    return f"{_BASE_SYSTEM}\n{law_context}"
